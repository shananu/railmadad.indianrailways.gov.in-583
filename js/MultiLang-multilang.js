function setlang(value, audioContent)
{
	var contextval=$("#contextval").val();
	
	var flag='Y';
	if(value.indexOf('#') != -1)
		{
		  flag='N';
		  value='na';
		  
		}

	$.ajax({
		method : 'GET',
		url :contextval+ '/LanguageModule',
		data : {
			
			lang:value,
			languageModuleType : "changeLanguage",
			audioContent: audioContent
			//fetchdatatype:'fetchlang'
		},
		success : function(responseText) {
	
			
			    const response = JSON.parse(responseText);
			    var obj = response.languageModelList;
			    var faqList = new Map();

						for(var i = 0; i< obj.length; i++)
							{
								
								$("."+obj[i].key).html(obj[i].value);
							if(document.getElementById(obj[i].key) != null){
							    document.getElementById(obj[i].key).innerHTML =
							    	obj[i].value ;
			          }
							if(obj[i].key.startsWith("que") || obj[i].key.startsWith("ans"))
							{
								faqList.set(obj[i].key , obj[i].value);
							}
						
							}
		
			if(flag=='Y')
				{
			 hindiconversion(faqList);
				}
			if(responseText.langCode=='hi')
			{
				 $("#covidimg").attr("src","images/covidh.jpg");

			}
			else{
				 $("#covidimg").attr("src","images/covide.jpg");
	
			}
			

		},
		error: function (xhr, status, thrownError) {
			console.log(status);
		}
	});

	
	
	
}



